import { describe, expect, it } from 'vitest';

import { buildStartupInitRoutine, type StartupInitRegion } from '../src/lowering/startupInit.js';

describe('PR652 startup init semantic routine', () => {
  it('keeps startup routine bytes stable for canonical startup-init region', () => {
    const region: StartupInitRegion = {
      copyEntries: [
        { kind: 'copy', destination: 0x4001, sourceOffset: 0, length: 3 },
        { kind: 'copy', destination: 0x4005, sourceOffset: 3, length: 2 },
      ],
      zeroEntries: [
        { kind: 'zero', destination: 0x4000, length: 1 },
        { kind: 'zero', destination: 0x4004, length: 1 },
      ],
      blob: [0x61, 0x62, 0x63, 0x01],
      encoded: [
        0x02, 0x00,
        0x01, 0x40, 0x00, 0x00, 0x03, 0x00,
        0x05, 0x40, 0x03, 0x00, 0x02, 0x00,
        0x02, 0x00,
        0x00, 0x40, 0x01, 0x00,
        0x04, 0x40, 0x01, 0x00,
        0x61, 0x62, 0x63, 0x01,
      ],
    };

    const bytes = buildStartupInitRoutine(0x5000, region, 0x4010);

    expect(bytes).toEqual([
      0x21, 0x00, 0x50,
      0x4e, 0x23, 0x46, 0x23,
      0x78, 0xb1, 0x28, 0x1b,
      0xc5, 0x5e, 0x23, 0x56, 0x23,
      0x4e, 0x23, 0x46, 0x23,
      0xe5,
      0x21, 0x18, 0x50,
      0x09, 0xe3,
      0x4e, 0x23, 0x46, 0x23,
      0xe3, 0xed, 0xb0,
      0xe1, 0xc1, 0x0b, 0x18, 0xe1,
      0x4e, 0x23, 0x46, 0x23,
      0x78, 0xb1, 0x28, 0x17,
      0xc5, 0x5e, 0x23, 0x56, 0x23,
      0x4e, 0x23, 0x46, 0x23,
      0x78, 0xb1, 0x28, 0x06,
      0xaf, 0x12, 0x13, 0x0b, 0x18, 0xf6,
      0xc1, 0x0b, 0x18, 0xe5,
      0xc3, 0x10, 0x40,
    ]);
  });
});
